// Freezes the shipped EXPLORE constellation into generated constants.
//
// The seeded layout (graph.ts) is a whole-field simulation: adding a 22nd node
// re-runs the forces over all nodes and MOVES every shipped word. Session 12
// breaks that coupling with a two-pass layout (graph.ts): the shipped prefix is
// read back from the constants THIS script emits, and appended nodes relax
// against that frozen field without disturbing it.
//
// This script bundles the current pure graph module with esbuild (its imports
// are type-only outside registry/palette, so no three.js/DOM/React is pulled),
// runs makeGraph() once, and writes the full-precision coordinates + edges to
// src/explore/frozen-layout.generated.ts.
//
// Run it ONLY to intentionally re-freeze (e.g. a sanctioned re-seed): appends
// must NOT change these constants. `npm run build` never calls it; the snapshot
// test guards the output.
import { build } from 'esbuild'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const SITE = resolve(here, '..')

async function loadGraphModule() {
  const result = await build({
    entryPoints: [join(SITE, 'src/explore/graph.ts')],
    bundle: true,
    format: 'esm',
    platform: 'node',
    write: false,
    logLevel: 'silent',
  })
  const code = result.outputFiles[0].text
  const url = 'data:text/javascript;base64,' + Buffer.from(code).toString('base64')
  return import(url)
}

const { makeGraph } = await loadGraphModule()
const { nodes, edges } = makeGraph()

const idOrder = nodes.map((n) => n.id).join('|')
// Full precision: JSON.stringify emits the shortest decimal that round-trips to
// the same double, so the reconstructed coords are byte-identical to today's.
const nodeLines = nodes
  .map((n) => `  { id: '${n.id}', x: ${n.x}, y: ${n.y}, z: ${n.z}, deg: ${n.deg} },`)
  .join('\n')
const edgeLines = edges
  .map((e) => `  { a: ${e.a}, b: ${e.b}, w: ${e.w}, implied: ${e.implied} },`)
  .join('\n')

const out = `// GENERATED by scripts/freeze-explore-layout.mjs — do not edit by hand.
// The frozen shipped EXPLORE field (Session 12). graph.ts reads the shipped
// prefix from here so appended nodes can relax against it without moving it.
// Re-run the generator ONLY to intentionally re-seed the constellation; the
// snapshot test fails if these drift from makeGraph()'s shipped prefix.

export interface FrozenNode {
  id: string
  x: number
  y: number
  z: number
  deg: number
}

export interface FrozenEdge {
  a: number
  b: number
  w: number
  implied: boolean
}

// The shipped ids in layout order. graph.ts checks the registry's first
// FROZEN_NODES.length ids against this as a PREFIX (appends extend past it).
export const FROZEN_ID_ORDER = '${idOrder}'

export const FROZEN_NODES: FrozenNode[] = [
${nodeLines}
]

export const FROZEN_EDGES: FrozenEdge[] = [
${edgeLines}
]
`

const target = join(SITE, 'src/explore/frozen-layout.generated.ts')
mkdirSync(dirname(target), { recursive: true })
writeFileSync(target, out)
console.log(`froze ${nodes.length} nodes + ${edges.length} edges -> src/explore/frozen-layout.generated.ts`)
